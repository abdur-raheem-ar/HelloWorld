
public class VariableDemo {

	public static void main(String[] args) {
		// VARIABLES
		
		//Data Types - Primitive Or Non primitive
		
		//Primitive - integers - byte, short, int, long -- Decimal - float, double -- boolean(true or false) -- char('')
		//Non primitive - String(""), Class, Object, Array, interface
		
		byte a = 10;
		short b = 120;
		int c = 123456;
		long d = 123456789;
		float e = 1.45182224555f;
		double f = 2.25415486545;
		boolean g = true;
		boolean h = false;
		char i = 'A';
		String j = "Hello World";
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(i);
		System.out.println(j);
		System.out.println("The value of a is : " + a);

	}

}
